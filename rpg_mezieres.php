						<h1>Réeducation posturale globale - Mézières</h1>	
							<p>La Rééducation Posturale Globale (RPG) est une méthode douce permettant de corriger dans la globalité les problèmes de postures adoptés par les mauvaises habitudes du quotidien. Cette méthode s'apparente à la méthode MEZIERES. Elle est basée sur l'analyse de l'anatomie fonctionnelle et la physiologie musculaire.</p>
								<h2>Principe de la méthode</h2>
									<p>La RPG repose sur un principe de base : toute activité musculaire, statique ou dynamique, entraîne un raccourcissement musculaire qu'il faut donc en permanence rééduquer en allongement. Cette rééducation prend en considération les différences fondamentales qui existent entre les muscles de la dynamique et ceux de la statique (antigravitaires).
									Cette méthode permet de redonner de l'élasticité aux tissus par des étirements actifs isométriques globaux à travers des postures. Elle va permettre de soigner des dysfonctions musculo-squelettiques du corps en corrigeant les rétractions musculaires et aponévrotiques par un traitement global, personnalisé et spécifique à chaque patient.
									Contrairement à beaucoup d'autres thérapies qui ne traitent qu'une partie du corps, la RPG prend en considération le corps dans son ensemble. C'est pour cette globalité que cette méthode est aussi efficace. Chaque patient est une entité unique.
									Chaque pathologie aura une répercussion différente sur chaque patient. Selon la pathologie et le patient, il y aura différentes compensations destinés à supprimer la douleur. Par exemple, lorsque l'on se fait une petite entorse de cheville, la compensation automatique et immédiate sera une boiterie pour soulager l'appui sur cette cheville. Mais il y aura d'autres compensations pouvant toucher le genou, la hanche voir le bassin et les lombaires. Il en est de même pour toutes les pathologies. C'est pour cela que la rééducation se doit d'être globale car les douleurs apparaissent la plupart du temps loin de la cause et de façon retardée.</p>
