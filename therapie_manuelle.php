								<p>Il s'agit d'une spécialité en kinésithérapie qui se rapproche de l'ostéopathie et qui s'intéresse au corps dans sa globalité.</p>
									<h2>Principe de la méthode</h2>
										<p>En thérapie manuelle on considère que la douleur provient d'un blocage ou hypomobilité articulaire. Un blocage articulaire s'accompagne d'un déséquilibre des tissus entourant l'articulation (muscles, ligaments, capsules, peau) causant des troubles posturaux. On utilise des techniques de mobilisation spécifique afin de redonner de la mobilité à l'articulation ; des techniques de levé de tension et d'inhibition afin de libérer les muscles ; des techniques d'étirements et de massages profonds afin de libérer les structures ligamentaires et aponévrotiques. La zone ainsi libérée, la douleur diminue puis disparaît.

										La thérapie manuelle s'adresse à toutes les douleurs d'origine rhumatologique, c'est à dire les arthroses, cervicalgies, dorsalgie, lombalgie, lumbago, canal carpien, tendinite, pubalgie.

										Elle s'adresse également aux pathologies post traumatiques comme l'entorse de cheville et les différents faux mouvements. Elle est donc utile aux sportifs suite à des chocs ou des efforts lors de leur pratique provoquant des blocages.

										Les résultats sont variables selon l'individu et la pathologie. Une seule séance peut parfois être suffisante afin de libérer l'articulation, mais dans les cas chroniques, le nombre de séance augmente.
										Il peut être intéressant de compléter la thérapie manuelle avec un traitement de fond en travaillant sur la posture avec notamment la rééducation posturale globale (RPG).</p>
